// Logical AND (&&) and Logical OR(||)
let a = true;
let b = true;
console.log(a && b);
console.log(a || b);

let c = false;
let d = true;
console.log(c && d);
console.log(c || d);

let e = false;
let f = false;
console.log(e && f);
console.log(e || f);

// Template literals
let name1 = "Melo";
let name2 = "Matthew";
console.log(name1 + " " + name2, `${name1} ${name2}`);

// Ternary operator
let isTrue = true;
console.log(isTrue ? "True" : "False");

